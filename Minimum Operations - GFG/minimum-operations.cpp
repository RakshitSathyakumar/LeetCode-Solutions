class Solution
{
  public:
    int minOperation(int n)
    {
        //code here.
        int N=n,cnt=0;
        while(N){
            if(N%2){
                N--;
            }else{
                N=N/2;
            }
            cnt++;
        }
        return cnt;
    }
};

